// Helper function to get random photo from a category
export const getRandomPhoto = (category: keyof typeof photoAssets) => {
  const photos = photoAssets[category];
  if (Array.isArray(photos)) {
    return photos[Math.floor(Math.random() * photos.length)];
  }
  return Object.values(photos)[Math.floor(Math.random() * Object.values(photos).length)];
};

// Helper function to get specific photo
export const getPhoto = (category: keyof typeof photoAssets, key?: string) => {
  const photos = photoAssets[category];
  if (key && typeof photos === 'object' && !Array.isArray(photos)) {
    return photos[key as keyof typeof photos];
  }
  return getRandomPhoto(category);
}; 